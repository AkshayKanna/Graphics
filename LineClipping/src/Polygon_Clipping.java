    public void clipLeft()
    {
        int x,y,x1,x2,y1,y2;
        Point p;
        int flag = 0;
        for (int i = 0; i < vertex.size()-1; i++) 
        {
            x1 = vertex.get(i).x;
            x2 = vertex.get(i+1).x;
            y1 = vertex.get(i).y;
            y2 = vertex.get(i+1).y;
           // System.out.println(" x1: "+x1+" y1: " +y1+" x2: "+x2+" y2: " +y2);
            if(x1 < xmin && x2 >= xmin)
            {
                x  = xmin;
                y = y1 + (x-x1)*(y2-y1)/(x2-x1);
                p = new Point(x,y);
                vertex1.add(p);
                vertex1.add(vertex.get(i+1));
                flag++;
            }
            else if(x1 >= xmin && x2 >= xmin) 
            {
                vertex1.add(vertex.get(i+1));
                flag++;
            }
            else if(x1 >= xmin && x2 < xmin) 
            {
                x  = xmin;
                y = y2 + (x-x2)*(y2-y1)/(x2-x1);
                p = new Point(x,y);
                vertex1.add(p);
                flag++;
            }
            
        }
        if(flag>0)
            vertex1.add(vertex1.get(0));
    }
